# Get user's name
name = input("Hi there! What's your name? ")

# Get user's age
age = input("Nice to meet you, " + name + "! How old are you? ")

# Get user's favorite hobby
hobby = input("What is your favorite hobby? ")

# Get user's favorite place
place = input("Where is your favorite place to visit? ")

# Generate a short story
story = "Once upon a time, there was a young person named " + name + ". " + \
        "At the age of " + age + ", " + name + " loved to spend their time " + hobby + ". " + \
        "One day, " + name + " decided to take a trip to " + place + ". " + \
        "They packed their bags and embarked on a journey to " + place + ". " + \
        "During their trip, " + name + " had many adventures, met new friends, and created lasting memories. " + \
        "At the end of the trip, " + name + " returned home with a heart full of joy and cherished memories of their favorite place, " + place + ". " + \
        "And they lived happily ever after."

# Print the story
print("\nHere's your short story:")
print(story)
